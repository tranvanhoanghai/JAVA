public class week1 {
    public static void main(String[] args) {
        int a = 20;
        int b = 5;

        double x = 27.475;
        double y = 7.22;

        System.out.println("a + b = " + (a + b));
        System.out.println("x + y = " + (x + y));

        System.out.println("a - b =  " + (a - b));
        System.out.println("x - y =  " + (x - y));

        System.out.println("a * b =  " + (a * b));
        System.out.println("x * y =  " + (x * y));

        System.out.println("a / b = " + (a / b));
        System.out.println("x / y = " + (x / y));

        System.out.println("a % b = " + (a % b));
        System.out.println("x % y = " + (x % y));

        System.out.println("=============================");

        System.out.println("a++ " + a++ +" | result "+ + a);
        System.out.println("a-- " + a-- +" | result "+ + a);

        System.out.println("++a " + ++a);
        System.out.println("--a " + --a);

    }
}